package com.library.borrowing.model;

import jakarta.persistence.*;

@Entity
@Table(name = "member_ref")
public class MemberReference {

    @Id
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
